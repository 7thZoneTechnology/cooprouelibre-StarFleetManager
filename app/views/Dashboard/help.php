<h2>Fonctionnalités</h2>

Le système de gestion permet de gérer <br />
<ul>
<li>les points de services,</li>
<li> les membres,</li>
<li> les vélos,</li>
<li>les prêts de vélo,</li>
<li>les réparations,</li>
<li>les pièces,</li>
<li>les inventaires de pièces,</li>
<li>les utilisateurs du système,</li>
<li>les droits des utilisateurs.</li>
</ul>

<h2>Types d'utilisateurs</h2>

Les types d'utilisateurs sont<br />

<ul>
<li>les administrateurs du système,</li>
<li>les gestionnaires de point de service,</li>
<li>les mécaniciens,</li>
<li>les prêteurs à un point de service,</li>
<li>les observateurs à un point de service.</li>
</ul>

<h2>Administrateurs</h2>

Ils peuvent<br />
<ul><li>ajouter un point de service,</li>
<li>ajouter un utilisateur,</li>
<li>ajouter un droit à un utilisateur,</li>
</ul>

<h2>Gestionnaires</h2>

Ils peuvent<br />
<ul><li>ajouter un horaire pour leur point de service,</li>
<li>ajouter un membre,</li>
<li>ajouter un droit à un utilisateur,</li>
<li>ajouter un vélo,</li>
<li>déplacer un vélo.</li>
</ul>



<h2>Mécaniciens</h2>

Ils peuvent<br />
<ul>
<li>gérer les pièces,</li>
<li>gérer les inventaires,</li>
<li>réparer un vélo.</li>
</ul>



<h2>Prêteurs</h2>

Ils peuvent<br />
<ul><li>
 prêter un vélo,</li>
<li>ajouter un membre,</li>
<li>signaler un vélo.</li>
</ul>


<h2>Observateurs</h2>

Ils peuvent<br />
<ul><li>
 voir les statistiques.</li></ul>

